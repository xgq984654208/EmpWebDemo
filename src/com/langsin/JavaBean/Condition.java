package com.langsin.JavaBean;

public class Condition {
	private String ename;
	private String gender;
	private String job;
	private String COMM;
	private String LeftSal;
	private String RightSal;
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getCOMM() {
		return COMM;
	}
	public void setCOMM(String COMM) {
		this.COMM = COMM;
	}
	public String getLeftSal() {
		return LeftSal;
	}
	public void setLeftSal(String leftSal) {
		LeftSal = leftSal;
	}
	public String getRightSal() {
		return RightSal;
	}
	public void setRightSal(String rightSal) {
		RightSal = rightSal;
	}

	
	
}
